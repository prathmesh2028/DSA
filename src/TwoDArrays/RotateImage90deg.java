package TwoDArrays;
public class RotateImage90deg {
    public static void main(String[] args) {
        int [][] arr = {{1,2,3,4} , {5,6,7,8} , {9,8,7,6} , {1,2,8,6}} ;
        printArr(arr);
        System.out.println("\n");

        for(int i = 0 ; i < arr.length ; i++){
            for(int j = 0 ; j < i ; j++){
                int temp = arr[i][j];
                arr[i][j] = arr[j][i];
                arr[j][i] = temp;
            }
        }
        for(int i = 0 ; i < arr.length ; i++){
            int a = 0;
            int b = arr[0].length - 1;
            while(a < b){
                int temp = arr[i][a];
                arr[i][a] = arr[i][b];
                arr[i][b] = temp;
                a++;
                b--;
            }
        }

        printArr(arr);

    }
    public static void printArr(int[][] arr){
        for(int i = 0 ; i < arr.length ; i++){
            for(int j = 0 ; j< arr[0].length ; j++){
                System.out.print(arr[i][j] + "  ");
            }
            System.out.println();
        }
    }

}
