package java_basics;

public class ArithmeticOperations {
    public static void main(String[] args) {
        int x = 5 ;
        double y = 8;
        // integer operation with integer always give you the integer
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        // single line comment
        /*
            this is a multiline comment
         */
        System.out.println(y/x);



        //modulus operator
        // %
        System.out.println(x%y);
    }
}
